<?php
declare(strict_types=1);

namespace Translator;

/**
 * Женско-мужской переводчик — словарь живёт на сервере.
 * Направления: 'f2m' (женский→мужской, «что имелось в виду»)
 *              'm2f' (мужской→женский).
 */
final class Translator
{
    /** Каждая пара: [фраза, перевод (настоящий смысл)] */
    private const SHE = [
        ['всё нормально',        'На самом деле не очень. Подойди обними и не делай вид, что не понял.'],
        ['всё хорошо',           'Так себе. Но принесёшь чаю — и резко станет хорошо.'],
        ['я не злюсь',           'Злюсь. Немножко. Ладно, сильно. Но быстро отойду.'],
        ['делай что хочешь',     'Это ловушка. Правильный ход — спросить, чего хочу я.'],
        ['как скажешь',          'Я уступила. Засчитай очко себе и не зазнавайся.'],
        ['мне ничего не нужно',  'Кроме внимания. И чтобы ты сам догадался. Удачи.'],
        ['нам надо поговорить',  'Без паники, ты не при чём. Ну, почти.'],
        ['ещё 5 минут',          'Это полчаса по земному времени. Налей пока кофе.'],
        ['я почти готова',       'Я в самом начале пути. Присядь, тебе тут жить.'],
        ['ничего не случилось',  'Случилось. Но я хочу, чтобы ты спросил сам, по-человечески.'],
        ['ты опять занят',       'Перевод: соскучилась. Оторвись уже от своих дел.'],
        ['делай как знаешь',     'Делай как я знаю. Подсказка: спроси меня.'],
        ['я в порядке',          'Не в порядке. Обними — и вопросов не останется.'],
        ['спасибо',              'Это честное спасибо, можешь выдохнуть.'],
    ];

    private const HE = [
        ['угу',                  'Слушаю. Кажется. Повтори последнее — и я снова с тобой.'],
        ['ага',                  'Это «да», просто на минималках. Я согласен.'],
        ['я почти приехал',      'Я только зашнуровываю ботинки. Но духом уже рядом.'],
        ['сейчас доиграю',       'Ещё раунд. Их будет три. Но потом я весь твой.'],
        ['я понял',              'Понял процентов на шестьдесят. Повтори, не сердись.'],
        ['нормально выглядишь',  'Ты шикарна. Просто мой словарный запас внезапно сдулся.'],
        ['не знаю',              'Без понятия, но звучу уверенно. Давай решим вместе?'],
        ['потом сделаю',         'Сделаю. Напомни раза три — и точно сделаю.'],
        ['это не дорого',        'Дорого. Но я уже придумал, как себя оправдать.'],
        ['я не голоден',         'Не голоден. Но твою тарелку проверю на всякий случай.'],
        ['всё нормально',        'Реально всё ок. У парней это иногда правда значит «ок».'],
        ['я тебя слушаю',        'Дай отложу телефон. Всё, ты в эфире, я весь внимание.'],
        ['ладно',                'Сдаюсь. Ты победила, и мне даже нравится.'],
        ['как хочешь',           'Честно без подвоха — выбирай, мне норм любой вариант.'],
    ];

    private const FALLBACK_SHE = [
        'Звучит мило, но за этим что-то кроется. Уточни.',
        'Перевод: это значит не совсем то, что написано 😏',
        'Здесь есть подтекст. Спроси прямо — целее будешь.',
    ];
    private const FALLBACK_HE = [
        'Перевод: всё буквально. Никакого подтекста, честно.',
        'Тут именно то, что сказано. Мужской язык простой.',
        'Смысл ровно один. Не ищи второе дно.',
    ];

    public function translate(string $text, string $direction): array
    {
        $dir  = $direction === 'm2f' ? 'm2f' : 'f2m';
        $dict = $dir === 'f2m' ? self::SHE : self::HE;
        $fb   = $dir === 'f2m' ? self::FALLBACK_SHE : self::FALLBACK_HE;

        $q = $this->norm($text);
        if ($q === '') {
            return ['text' => '', 'exact' => false, 'empty' => true, 'direction' => $dir];
        }

        // точное совпадение
        foreach ($dict as [$k, $v]) {
            if ($this->norm($k) === $q) {
                return ['text' => $v, 'exact' => true, 'empty' => false, 'direction' => $dir];
            }
        }
        // ключ содержится во вводе или наоборот
        foreach ($dict as [$k, $v]) {
            $nk = $this->norm($k);
            if ($nk !== '' && (mb_strpos($q, $nk) !== false || mb_strpos($nk, $q) !== false)) {
                return ['text' => $v, 'exact' => true, 'empty' => false, 'direction' => $dir];
            }
        }
        // вольный перевод — детерминированно по содержимому (без рандома на каждый запрос)
        $pick = $fb[crc32($q) % count($fb)];
        return ['text' => $pick, 'exact' => false, 'empty' => false, 'direction' => $dir];
    }

    /** список фраз для разговорника */
    public function phrasebook(string $direction): array
    {
        $dict = ($direction === 'm2f' ? self::HE : self::SHE);
        return array_map(static fn($p) => $p[0], array_slice($dict, 0, 6));
    }

    /** полные пары [фраза, перевод] направления — для прогрева кэша */
    public function phrasebookFull(string $direction): array
    {
        return $direction === 'm2f' ? self::HE : self::SHE;
    }

    private function norm(string $s): string
    {
        $s = mb_strtolower(trim($s));
        $s = str_replace('ё', 'е', $s);
        $s = preg_replace('/[.,!?;:"\'()«»…\-—]+/u', ' ', $s) ?? $s;
        $s = preg_replace('/\s+/u', ' ', $s) ?? $s;
        return trim($s);
    }
}
